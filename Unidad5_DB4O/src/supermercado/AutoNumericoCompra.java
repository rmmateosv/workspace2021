package supermercado;

public class AutoNumericoCompra {
	private int codigo;

	public AutoNumericoCompra() {
		super();
	}
	
	public AutoNumericoCompra(int codigo) {
		super();
		this.codigo = codigo;
	}

	public int getCodigo() {
		return codigo;
	}

	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	
	
}
